        function initMap1() {
            var input = document.getElementById('from');
            var autocomplete = new google.maps.places.Autocomplete(input, { componentRestrictions: {country: 'in'} });
            autocomplete.setFields(['geometry', 'name', 'address_components']);
            google.maps.event.addListener(autocomplete, 'place_changed', function() {
                var place = autocomplete.getPlace();
                console.log(place.name + ' ' + place.geometry.location.lat() + ' ' + place.geometry.location.lng());
                console.log(JSON.stringify(place.address_components));
                var redirect = '<?=WEB_URL?>search-outlet.php?lat=' + place.geometry.location.lat() +
                    '&long=' + place.geometry.location.lng() + '&location=' + encodeURIComponent(place.name.toLowerCase() + '|'+$('#from').val());
                // window.location.href = redirect;
                $('.btnSearch').attr('onclick', 'window.location.href="'+redirect+'"');
            });               
        }
